def deep_merge(base, new_data):
    """Recursively merge new_data into base dict."""
    for key, value in new_data.items():
        if key in base and isinstance(base[key], dict) and isinstance(value, dict):
            deep_merge(base[key], value)
        else:
            base[key] = value
    return base

